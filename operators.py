# Arithmetic Operators
a = 21
b = 5
print(f'{a} + {b} = {a + b}')
print(f'{a} - {b} = {a - b}')
print(f'{a} * {b} = {a * b}')
print(f'{a} / {b} = {a / b}') # 标准除法
print(f'{a} // {b} = {a // b}')  # 整除，向下取整到最近的整数
print(f'{a} % {b} = {a % b}')  # 取模，返回余数
print(f'{a} ** {b} = {a ** b}')  # 幂运算，a 的 b 次方